let arr=[50, 20, 10, -1,"Tanya"];
console.log(arr);

console.log(arr.length); // length is not a function, it is a property

arr.push(10); // add element at the end
console.log(arr);

arr.pop(); // remove element from the end
console.log(arr);

arr.shift(); // remove element from start
console.log(arr);

arr.unshift(100); // add element at the start
console.log(arr);

arr.reverse(); // reverse the element
console.log(arr);

console.log(arr.indexOf(100)); // returns the index of the element

console.log(arr.indexOf(102)); // returns the index of the element

console.log(arr[2]); // access the element at the index 2



